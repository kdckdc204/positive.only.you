function getPool() {
  if (currentCategory === "all") {
    return Object.values(quoteData).flat();
  }
  return quoteData[currentCategory];
}

function getRandomQuote() {
  const pool = getPool();
  return pool[Math.floor(Math.random() * pool.length)];
}

function draw() {
  claw.classList.add("down");
  capsule.classList.remove("open");

  setTimeout(() => {
    capsule.classList.add("open");
  }, 650);

  setTimeout(() => {
    const name = localStorage.getItem("positiveClawName");
    const quote = getRandomQuote();
    currentQuote = name ? `${name}님,\n\n${quote}` : quote;
    result.innerText = currentQuote;
    categoryLabel.innerText = categoryNames[currentCategory];
    claw.classList.remove("down");
  }, 1100);
}

function copyText() {
  const text = result.innerText;
  navigator.clipboard.writeText(text).then(() => showToast("문구가 복사되었습니다."));
}

